int		ft_atoi(char *str)
{
	int		nb;
	int		sign;
	int		i;

	i = 0;
	sign = 1;
	nb = 0;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] != '\0')
	{
		if (str[i] < '0' || str[i] > '9')
			break ;
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (nb * sign);
}
